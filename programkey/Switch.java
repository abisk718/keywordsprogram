package programkey;

public class Switch {
	  public static void main(String[] args) {
int day = 4;
switch (day) {
  case 1:
    System.out.println("Today is Saturday");
    break;
  case 2:
    System.out.println("Today is Sunday");
    break;
  default:
    System.out.println("Looking forward to the Weekend");
}
}
}